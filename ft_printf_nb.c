#include "ft_printf.h"

void	ft_printf_nb(unsigned int nb, int *i)
{
	if (nb >= 10)
	{
		ft_printf_nb(nb / 10, i);
		nb %= 10;
	}
	ft_printf_char(nb + '0', i);
}
